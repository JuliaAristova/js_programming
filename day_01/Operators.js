//Arithmetic operators
console.log(5 + 3); // Output: 8
console.log(10 - 2); // Output: 8   
console.log(4 * 2); // Output: 8
console.log(10 / 4); // Output: 2.5
console.log(17 % 9); // Output: 8 (remainder of 17 divided by 9)
console.log(2 ** 3); // Output: 8 (2 raised to the power of 3)

//Assignment operators
let x
console.log(x);
x = 5; // Assigns 5 to x
console.log(x);

x+= 3; // Adds 3 to x (x = x + 3)
console.log(x); // Output: 8
x -= 2; // Subtracts 2 from x (x = x - 2)
console.log(x); // Output: 6

//Relational operators
console.log(5 > 3); // Output: true
console.log(5 < 3); // Output: false 
console.log((1+1) >= 2); // Output: true (2 is greater than or equal to 2)
console.log(5 <= 5); // Output: true (5 is less than or equal`to 5)

console.log("-------------");
let j = 10;
let y = "10";
console.log(j == y); // Output: true (value equality)
console.log(j === y); // Output: true (strict equality, checks value and type)

console.log("20" != 20); // Output: true (value is not equal, but type is different)
console.log("20" !== 20); // Output: true (strict inequality, checks value and type)

console.log("-------------");
//Logical operators
console.log(true&& true); // Output: true (logical AND)
console.log(true && false); // Output: false (logical AND)
console.log(true || false); // Output: true (logical OR)
console.log(false || false); // Output: false (logical OR)
console.log(!true); // Output: false (logical NOT)
